public class Filme {
    private int id;
    private String titulo;
    private String genero;
    private String diretor;
    private int ano;
    private String ator1;
    private String ator2;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the titulo
     */
    public String getTitulo() {
        return titulo;
    }

    /**
     * @param titulo the titulo to set
     */
    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    /**
     * @return the genero
     */
    public String getGenero() {
        return genero;
    }

    /**
     * @param genero the genero to set
     */
    public void setGenero(String genero) {
        this.genero = genero;
    }

    /**
     * @return the diretor
     */
    public String getDiretor() {
        return diretor;
    }

    /**
     * @param diretor the diretor to set
     */
    public void setDiretor(String diretor) {
        this.diretor = diretor;
    }

    /**
     * @return the ano
     */
    public int getAno() {
        return ano;
    }

    /**
     * @param ano the ano to set
     */
    public void setAno(int ano) {
        this.ano = ano;
    }

    /**
     * @return the ator1
     */
    public String getAtor1() {
        return ator1;
    }

    /**
     * @param ator1 the ator1 to set
     */
    public void setAtor1(String ator1) {
        this.ator1 = ator1;
    }

    /**
     * @return the ator2
     */
    public String getAtor2() {
        return ator2;
    }

    /**
     * @param ator2 the ator2 to set
     */
    public void setAtor2(String ator2) {
        this.ator2 = ator2;
    }
}
